print('*** Operadores Asignacion Compuestos ***')
a, b = 10, 15
print(f'Valor inicial a: {a}, b: {b}')

# Operador copuesto de suma +=
a += b # a = a + b
print(f'Operador a += b es: {a}')

# Operador copuesto de resta -=
a = 10
a -= b # a = a - b
print(f'Operador a -= b es: {a}')

# Operador copuesto de multiplicacion *=
a = 10
a *= b # a = a * b
print(f'Operador a *= b es: {a}')

# Operador copuesto de division /=
a = 10
a /= b # a = a / b
print(f'Operador a /= b es: {a:.2f}')