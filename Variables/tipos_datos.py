# Ejemplo de tipos de datos en Python

# Entero
edad = 33
print('Edad:', edad)

# Flotante
altura = 1.74
print('Altura:', altura)

# Cadena de texto
nombre = 'Joan'
print('Nombre:', nombre)

# Tipo boolean
es_estudiante = False
print('Es estudiante?:', es_estudiante)

# Tipo None, ausencia de valor
direccion = None
print('direccion:', direccion)